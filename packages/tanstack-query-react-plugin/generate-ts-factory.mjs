async function main() {
  // const tsFactoryCodeGenerator from '@openapi-qraft/ts-factory-code-generator';
  // getSourceFileFactoryCode
  serviceOperationFiles.forEach((file) => {
    const sourceFilePath = getSourceFileFactoryCode(
      path.join('../tanstack-query-react-types/src/service-operation', file)
    );

    const sourceFileBaseName = path.basename(file, path.extname(file));

    const fileCode = `
/** This file was auto-generated by "@openapi-qraft/tanstack-query-react-plugin" codegen script. */
import ts from "typescript";
const factory = ts.factory;
export const ${camelcase('create-' + sourceFileBaseName + '-nodes')} = () => ${sourceFilePath}
`;

    fs.writeFileSync(
      path.join('src', 'ts-factory', 'service-operation.generated', file),
      fileCode.trim() + '\n'
    );
  });
}

main().catch((error) => {
  console.error(error);
  process.exit(1);
});
